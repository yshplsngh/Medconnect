import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date:{
    type: Date,
    default: Date.now
},
});

export const userModel = mongoose.model("user",userSchema);